    DESCRIPCION_PROD VARCHAR(100) NOT NULL,
    PRECIO FLOAT(6,2) NOT NULL,
    IMAGEN VARCHAR(100) NOT NULL,
    INGREDIENTES VARCHAR(200) NOT NULL
